public class pattern3 {
    
        public static void main(String[] args) {
            
            
            for( int star =1;star<=4;star++){
                for(int j=1;j<=4-star;j++){
                System.out.print(" ");
            }
            for(int j=1;j<=star;j++){
                System.out.print("*");

            } System.out.println();
}}}