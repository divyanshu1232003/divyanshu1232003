public class _01triangle {
    public static void main(String[] args) {
      
            int i =5;
            int sum ;
            for(i =1;i <= 4;i++){
                for(int j=1;j<=i;j++){
                    sum = i+j;
                    if((sum%2)==0){
                        System.out.print("1");
                    }else{
                        System.out.print("0");
                    }
                   
            } System.out.println();
          
}}}
